<script type="text/javascript">
	function registernowvalidation() {
		if (document.registernow.name.value == '') {
			alert('Please enter your Name')
			document.registernow.name.focus();
			return false;
		}
		if (document.registernow.emailid.value == '') {
			alert('Please enter your Email ID')
			document.registernow.emailid.focus();
			return false;
		}
		if (!document.registernow.emailid.value.match(/^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/)) {
			alert("Please enter a valid Email ID.");
			document.registernow.emailid.focus();
			return false;
		}
		if (document.registernow.phoneno.value == "") {
			alert('Please enter Phone No.');
			document.registernow.phoneno.focus();
			return false;
		}
		if (document.registernow.citycountry.value == "") {
			alert('Please enter City and Country.');
			document.registernow.citycountry.focus();
			return false;
		}
		if (document.registernow.message.value == "") {
			alert('Please enter Message.');
			document.registernow.message.focus();
			return false;
		}
	}
</script>
